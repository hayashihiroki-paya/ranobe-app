// features/like/components/LikeButton.tsx

"use client"
// ↑ このコンポーネントは Client Component
//   ReactのuseStateやクリックイベントを使うため必須

// ---------------------------------------------
// React Hooks
// ---------------------------------------------
import { useState } from "react"


// ---------------------------------------------
// Like API 呼び出し関数
// ---------------------------------------------
import { likeBook, unlikeBook } from "@/lib/api/like"


// ---------------------------------------------
// 楽天APIから取得した本の型
// ---------------------------------------------
import { RakutenBook } from "@/types/book"


// ---------------------------------------------
// コンポーネントのProps型
// ---------------------------------------------
type Props = {

  // 楽天APIの本データ
  book: RakutenBook

  // すでにお気に入り済みかどうか
  // （サーバー側で判定して渡す）
  initialLiked: boolean
}


// ---------------------------------------------
// Likeボタンコンポーネント
// ---------------------------------------------
export default function LikeButton({ book, initialLiked }: Props) {

  // -------------------------------------------------
  // Reactの状態管理
  // liked = 現在のお気に入り状態
  // -------------------------------------------------
  const [liked, setLiked] = useState(initialLiked)

  // -------------------------------------------------
  // ボタンクリック処理
  // -------------------------------------------------
  const handleClick = async () => {

    try {

      // -------------------------------------------------
      // すでにお気に入り済みの場合
      // -------------------------------------------------
      if (liked) {

        // お気に入り解除API
        await unlikeBook(book.isbn)

        // React状態更新
        // → UIが即更新される
        setLiked(false)

      } else {

        // -------------------------------------------------
        // 未お気に入りの場合
        // -------------------------------------------------

        // お気に入り登録API
        await likeBook(book)

        // React状態更新
        setLiked(true)
      }

    } catch (err) {

      // -------------------------------------------------
      // APIエラー処理
      // -------------------------------------------------

      console.error(err)

      alert("エラーが発生しました")
    }
  }


  // -------------------------------------------------
  // UI表示
  // -------------------------------------------------
  return (

    // クリックイベント登録
    <button onClick={handleClick}>

      {/* liked状態で表示切り替え */}
      {liked
        ? "❤️ お気に入り解除"
        : "🤍 お気に入り登録"
      }

    </button>
  )
}